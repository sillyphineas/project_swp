    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        //Authorize
        HttpSession session = request.getSession(false);
        User user = null;
        if (session != null) {
            user = (User) session.getAttribute("user");
        }
        if (!Authorize.isAccepted(user, "/CustomerDetailController")) {
            request.getRequestDispatcher("WEB-INF/views/404.jsp").forward(request, response);
            return;
        }
        int customerId = Integer.parseInt(request.getParameter("id"));
        DAOUser dao = new DAOUser();
        
        // Lấy thông tin khách hàng theo ID
        User customer = dao.getUserById(customerId);
        request.setAttribute("customer", customer);
        
        // Lấy lịch sử thay đổi
        List<Map<String, Object>> changeHistory = dao.getCustomerChangeHistory(customerId);
        request.setAttribute("changeHistory", changeHistory);

        request.getRequestDispatcher("/WEB-INF/views/customerDetail.jsp").forward(request, response);
    }
    

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        processRequest(request, response);
    }

